Write-Host "Iniciando firma de scripts Transcribe..." -ForegroundColor Cyan

# THUMBPRINT DEL CERTIFICADO (FIJO Y SEGURO)
$Thumbprint = "9A307DA02415E7208E883F7B68CB3F00F27E0C75"

# Obtener certificado
$cert = Get-ChildItem Cert:\CurrentUser\My |
        Where-Object { $_.Thumbprint -eq $Thumbprint }

if (-not $cert) {
    Write-Host "Certificado no encontrado." -ForegroundColor Red
    exit 1
}

# Carpeta base del proyecto
$BaseDir = Split-Path -Parent $MyInvocation.MyCommand.Definition

# Buscar todos los .ps1
$scripts = Get-ChildItem -Path $BaseDir -Recurse -Filter "*.ps1"

foreach ($script in $scripts) {
    Write-Host "Firmando: $($script.FullName)"
    Set-AuthenticodeSignature `
        -FilePath $script.FullName `
        -Certificate $cert `
        -TimestampServer "http://timestamp.digicert.com" | Out-Null
}

Write-Host "Firma completada correctamente." -ForegroundColor Green

